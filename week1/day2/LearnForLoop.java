package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		//System.out.println("1");
		//System.out.println("1");
		
		//shorctut for forloop--->type for and do ctrl+space
		//initialization,condition,iteration
		for (int i = 1; i <=10; i++) {
			System.out.println(i);
			
		}
		System.out.println("---------------------------");
		for (int i = 10; i >=1; i--) {
			System.out.println(i);
		}
		
	}

}
